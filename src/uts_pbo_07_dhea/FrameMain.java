    private void selectData() {
        //To change body of generated methods, choose Tools | Templates.
        String kolom[] = {"NamaPemesan","NoID","StationAwal","StationTujuan","TanggalBerangkat","JenisKereta","WaktuBerangkat","TempatDuduk","Harga"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_pemesan";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
                String NamaPemesan = rs.getString(1);
                String NoID = rs.getString(2);
                String StationAwal = rs.getString(3);
                String StationTujuan = rs.getString(4);
                
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String TanggalBerangkat = dateFormat.format(txttanggal.getDate());
                TanggalBerangkat = rs.getString(5);
                
                String JenisKereta = "";
                if ("Eksekutif".equals(rs.getString(6))){
                    JenisKereta = "Eksekutif";
                }else {
                    JenisKereta = "Ekonomi";
                }
                String WaktuBerangkat = rs.getString(7);
                String TempatDuduk = rs.getString(8);
                String Harga = rs.getString(9);
                String data[] = {NamaPemesan,NoID,StationAwal,StationTujuan,TanggalBerangkat,JenisKereta,WaktuBerangkat,TempatDuduk,Harga};
                dtm.addRow(data);
            }
        }
        catch(SQLException ex){
            Logger.getLogger(FrameMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblData.setModel(dtm);     
    }
    
