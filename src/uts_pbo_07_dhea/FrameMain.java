    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(txttanggal.getDate());
        
        if ("".equals(txtNama.getText()) || "".equals(txtId.getText()) ||
                "".equals(cbAwal.getSelectedItem()) || "".equals(cbTujuan.getSelectedItem()) || tanggal.equals("") 
                || "".equals(cbWaktu.getSelectedItem()) || "".equals(cbDuduk.getSelectedItem()) || "".equals(txtHarga.getText()))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data","Error", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            String JK = "";
                if (rdEksekutif.isSelected())
                {
                    JK = "Eksekutif";
                }else
                {
                    JK = "Ekonomi";
                }
            String SQL = "INSERT tb_pemesan (NamaPemesan,NoID,StationAwal,StationTujuan,TanggalBerangkat,JenisKereta,WaktuBerangkat,TempatDuduk,Harga)"
                    + "VALUES('"+txtNama.getText()+"','"+txtId.getText()+"','"+cbAwal.getSelectedItem()+"','"+cbTujuan.getSelectedItem()+"','"+tanggal+"','"+JK+"',"
                    + "'"+cbWaktu.getSelectedItem()+"','"+cbDuduk.getSelectedItem()+"','"+txtHarga.getText()+"')";
            int status = KoneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else
            {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

